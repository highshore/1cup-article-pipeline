create or replace function public.is_dashboard_authorized()
returns boolean
language sql
security definer
set search_path = public
as $$
  select auth.role() = 'service_role'
    or exists (
      select 1
      from public.dashboard_user_access
      where user_id = auth.uid()
        and role in ('supreme_leader', 'authorized')
    );
$$;

