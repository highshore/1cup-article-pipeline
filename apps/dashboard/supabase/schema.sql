create or replace function public.article_bot_enqueue_pipeline_request(
  p_trigger_source text,
  p_requested_by text,
  p_cadence text,
  p_workflow_key text,
  p_payload_json jsonb,
  p_backend_metadata_json jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  request_id bigint;
begin
  if not public.is_dashboard_authorized() then
    raise exception 'not-authorized' using errcode = '42501';
  end if;

  if exists (
    select 1
    from public.pipeline_run_requests
    where status in ('requested', 'claimed', 'submitted', 'running', 'cancelling')
    limit 1
  ) or exists (
    select 1
    from public.collection_runs
    where status in ('queued', 'running')
    limit 1
  ) then
    return jsonb_build_object('ok', false, 'reason', 'active-run-exists');
  end if;

  insert into public.pipeline_run_requests (
    pipeline_key,
    workflow_key,
    trigger_source,
    requested_by,
    executor,
    status,
    payload_json,
    requested_at,
    backend_metadata_json
  )
  values (
    'article-bot',
    p_workflow_key,
    p_trigger_source,
    p_requested_by,
    'local',
    'requested',
    p_payload_json,
    now(),
    p_backend_metadata_json
  )
  returning id into request_id;

  return jsonb_build_object('ok', true, 'requestId', request_id);
end;
$$;

create or replace function public.article_bot_claim_next_request()
returns public.pipeline_run_requests
language plpgsql
security definer
set search_path = public
as $$
declare
  claimed public.pipeline_run_requests;
begin
  if not public.is_dashboard_authorized() then
    raise exception 'not-authorized' using errcode = '42501';
  end if;

  with next_request as (
    select id
    from public.pipeline_run_requests
    where status = 'requested'
    order by requested_at asc, id asc
    for update skip locked
    limit 1
  )
  update public.pipeline_run_requests request
  set status = 'claimed',
      claimed_at = now()
  from next_request
  where request.id = next_request.id
  returning request.* into claimed;

  return claimed;
end;
$$;

create or replace function public.article_bot_create_collection_run(p_request_id bigint)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  request_row public.pipeline_run_requests;
  payload jsonb;
  run_id bigint;
  source_scope text;
begin
  if not public.is_dashboard_authorized() then
    raise exception 'not-authorized' using errcode = '42501';
  end if;

  select *
  into request_row
  from public.pipeline_run_requests
  where id = p_request_id
  limit 1;

  if request_row.id is null then
    raise exception 'request-not-found';
  end if;

  payload := coalesce(request_row.payload_json, '{}'::jsonb);
  select string_agg(value, ',')
  into source_scope
  from jsonb_array_elements_text(coalesce(payload->'sources', '["wsj.com", "ft.com"]'::jsonb));

  insert into public.collection_runs (
    request_id,
    cadence,
    run_date,
    status,
    source_scope,
    executor,
    started_at,
    created_at
  )
  values (
    request_row.id,
    coalesce(payload->>'cadence', 'daily'),
    (now() at time zone 'Asia/Seoul')::date,
    'running',
    coalesce(source_scope, 'wsj.com,ft.com'),
    request_row.executor,
    now(),
    now()
  )
  returning id into run_id;

  update public.pipeline_run_requests
  set collection_run_id = run_id,
      status = 'running',
      started_at = now()
  where id = request_row.id;

  insert into public.pipeline_run_steps (run_id, stage_key, stage_label, stage_detail, stage_order, status, updated_at)
  values
    (run_id, 'load_settings', 'Load criteria', 'Read target sources, evaluation criteria, schedule metadata, and run options.', 0, 'queued', now()),
    (run_id, 'crawl_sources', 'Crawl WSJ / FT', 'Collect fresh stories from Wall Street Journal and Financial Times sections.', 1, 'queued', now()),
    (run_id, 'normalize_articles', 'Normalize articles', 'Canonicalize URLs, fold duplicates, and prepare crawler rows for evaluation.', 2, 'queued', now()),
    (run_id, 'evaluate_articles', 'Evaluate fit', 'Score articles against lesson value, C1 vocabulary, discussion potential, and business relevance.', 3, 'queued', now()),
    (run_id, 'process_articles', 'Process outputs', 'Run translation, summaries, discussion prompts, vocabulary extraction, and media sidecars.', 4, 'queued', now()),
    (run_id, 'deliver_kakao', 'Notify Kakao', 'Build a compact Kakao-ready brief with links back to the dashboard.', 5, 'queued', now());

  return run_id;
end;
$$;

create or replace function public.article_bot_cancel_active_pipeline_request(p_requested_by text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  request_row public.pipeline_run_requests;
  next_status text;
begin
  if not public.is_dashboard_authorized() then
    raise exception 'not-authorized' using errcode = '42501';
  end if;

  select *
  into request_row
  from public.pipeline_run_requests
  where status in ('requested', 'claimed', 'submitted', 'running', 'cancelling')
  order by requested_at desc, id desc
  limit 1;

  if request_row.id is null then
    return jsonb_build_object('ok', false, 'reason', 'no-active-request');
  end if;

  next_status := case when request_row.status in ('requested', 'claimed') then 'cancelled' else 'cancelling' end;

  update public.pipeline_run_requests
  set status = next_status,
      finished_at = case when next_status = 'cancelled' then coalesce(finished_at, now()) else finished_at end,
      cancel_requested_at = coalesce(cancel_requested_at, now()),
      cancel_requested_by = coalesce(cancel_requested_by, p_requested_by),
      error_text = case when next_status = 'cancelled' then coalesce(error_text, 'Cancelled from ' || p_requested_by) else error_text end
  where id = request_row.id;

  if next_status = 'cancelled' and request_row.collection_run_id is not null then
    update public.collection_runs
    set status = 'cancelled',
        finished_at = coalesce(finished_at, now()),
        notes = coalesce(notes, 'Cancelled from ' || p_requested_by)
    where id = request_row.collection_run_id;

    update public.pipeline_run_steps
    set status = 'cancelled',
        updated_at = now()
    where run_id = request_row.collection_run_id
      and status in ('queued', 'running');
  end if;

  return jsonb_build_object('ok', true, 'requestId', request_row.id, 'status', next_status);
end;
$$;

